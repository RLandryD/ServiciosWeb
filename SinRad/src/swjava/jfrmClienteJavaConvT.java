    private static double convCentAFahr(double gCent) {
        jaxws.SWConverTemps_Service service = new jaxws.SWConverTemps_Service();
        jaxws.SWConverTemps port = service.getSWConverTempsPort();
        return port.convCentAFahr(gCent);
    }

    private static double convFahrACent(double gFahr) {
        jaxws.SWConverTemps_Service service = new jaxws.SWConverTemps_Service();
        jaxws.SWConverTemps port = service.getSWConverTempsPort();
        return port.convFahrACent(gFahr);
    }

