    private void jbtConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtConvertirActionPerformed
        double nGrados = 0.0;
        SWConverTempsService service; //Clase derivada de service
        SWConverTemps port;

        try {
//Crear un objeto de la clase que implementa el servlet
            service = new SWConverTempsService();
            port = service.getSWConverTempsPort();
//Obtener el valor escrcito en la caja de texto
            nGrados = Double.parseDouble(jtfGrados.getText());
        } catch (NumberFormatException ex) {
            System.out.println("Dato grados incorrecto\n");
            return;
        } catch (Exception ex) {
            System.out.println("No se puede acceder al servicio\n");
            return;
        }
        if (jrbFahrACent.isSelected()) {
            nGrados = port.ConvFahrACent(nGrados);
        }
        if (jrbCentAFahr.isSelected()) {
            nGrados = port.ConvCentAFahr(nGrados);
        }
        jtfGrados.setText(Double.toString(nGrados));
    
    }//GEN-LAST:event_jbtConvertirActionPerformed

